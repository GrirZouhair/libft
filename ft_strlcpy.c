#include "libft.h"

size_t	ft_strlcpy(char *dest, char *src, size_t size)
{
	size_t			st_len;
	size_t			i;

	i = 0;
	st_len = ft_strlen(src);
	if (size > 0)
	{
		while (src[i] && i < size - 1)
		{
			dest[i] = src[i];
			i++;
		}
		dest[i] = '\0';
	}
	return (st_len);
}
